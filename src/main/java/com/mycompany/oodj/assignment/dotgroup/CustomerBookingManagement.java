    // Display bookings in the JTable
    private void displayBookings(ArrayList<Period> bookings) {
        DefaultTableModel model = (DefaultTableModel) tblBookings.getModel();
        model.setRowCount(0); // Clear existing rows

        // Add each booking to the table
        for (Period booking : bookings) {
            if(booking.getBookedBy() == null){
                continue;
            }
            if(booking.getBookedBy().getUsername().equals(currentCustomer.getUsername()) || currentCustomer == null){
                model.addRow(new Object[]{
                    booking.getTitle(),
                    booking.getHall().getName(),
                    booking.getStartTime().toLocalDate(),
                    booking.getStartTime().toLocalTime() + " - " + booking.getEndTime().toLocalTime(),
                    booking.getStatus()
                });
            }
        }

        tblBookings.revalidate();
        tblBookings.repaint();
        scrollPaneBookings.revalidate();
        scrollPaneBookings.repaint();
    }

