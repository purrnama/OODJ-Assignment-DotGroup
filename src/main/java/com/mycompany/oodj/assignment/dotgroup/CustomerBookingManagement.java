    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        Date selectedDate = dateChooser.getDate();
        if (selectedDate != null) {
            ArrayList<Period> filteredBookings = new ArrayList<>();
            ArrayList<Period> bookings = fileOperation.read(FileType.SCHEDULE);
            LocalDate selectedLocalDate = selectedDate.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

            for (Period booking : bookings) {
                LocalDate bookingDate = booking.getStartTime().toLocalDate();
                if (bookingDate.equals(selectedLocalDate)) {
                    filteredBookings.add(booking);
                }
            }

            displayBookings(filteredBookings);
        }
    }//GEN-LAST:event_btnFilterActionPerformed

